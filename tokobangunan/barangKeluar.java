    private void loadTable() {
       // membuat tampilan table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Id Transaksi");
        model.addColumn("Penanggung Jawab");
        model.addColumn("Nama Barang");
        model.addColumn("Jumlah");
        model.addColumn("Tanggal");
        // memanggil data di SQL
        try {
            int no = 1;
            String SQL = "SELECT penjualan.id_penjualan, pegawai.nama_pegawai, barang.nama_barang, detail_penjualan.qty, penjualan.tanggal FROM penjualan JOIN detail_penjualan ON penjualan.id_penjualan = detail_penjualan.id_penjualan JOIN barang ON barang.id_barang = detail_penjualan.id_barang JOIN pegawai ON pegawai.id_pegawai = penjualan.id_pegawai ORDER BY tanggal DESC";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(SQL);
            while (res.next()) {
                model.addRow(new Object[] {
                no++,res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                });    
            }
            table_barangKeluar.setModel(model);
        } catch (Exception e) {
        }
    }
    
