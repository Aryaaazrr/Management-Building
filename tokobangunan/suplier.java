    private void table_suplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_suplierMouseClicked
        // Menampilkan baris data ke dalam form pengisisan
        int baris = table_suplier.rowAtPoint(evt.getPoint());
        String nim = table_suplier.getValueAt(baris, 1).toString();
        txt_idSuplier.setText(nim);
        txt_idSuplier.disable();
        if (table_suplier.getValueAt(baris, 2)==null) {
            txt_NamaSuplier.setText("");
            txt_cari.setText("");
        } else {
            txt_NamaSuplier.setText(table_suplier.getValueAt(baris, 2).toString());
        }
        if (table_suplier.getValueAt(baris, 3)==null) {
            txt_alamat.setText("");
        } else {
            txt_alamat.setText(table_suplier.getValueAt(baris, 3).toString());
        }
        if (table_suplier.getValueAt(baris, 4)==null) {
            txt_noHP.setText("");
        } else {
            txt_noHP.setText(table_suplier.getValueAt(baris, 4).toString());
        }
    }//GEN-LAST:event_table_suplierMouseClicked

