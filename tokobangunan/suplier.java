            String sql = "UPDATE suplier SET nama_suplier = '"+txt_NamaSuplier.getText()+"', alamat = '"+txt_alamat.getText()+"', no_telp = '"+txt_noHP.getText()+"' WHERE id_suplier = '"+txt_idSuplier.getText()+"'";
