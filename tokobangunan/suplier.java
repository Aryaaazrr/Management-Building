    private void loadTable() {
        String value = txt_cari.getText();
        // membuat tampilan table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Id Suplier");
        model.addColumn("Nama Suplier");
        model.addColumn("Alamat");
        model.addColumn("No Handphone");
        // memanggil data di SQL
        try {
            int no = 1;
            String SQL = "SELECT DISTINCT suplier.id_suplier, suplier.nama_suplier, suplier.alamat, suplier.no_telp "
                    + "FROM suplier WHERE nama_suplier like '%"+value+"%'";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(SQL);
            while (res.next()) {
                model.addRow(new Object[] {
                no++,res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                });    
            }
            table_suplier.setModel(model);
        } catch (Exception e) {
        }}

