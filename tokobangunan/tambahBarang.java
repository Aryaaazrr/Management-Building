    private void table_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_barangMouseClicked
        // Menampilkan baris data ke dalam form pengisisan
        int baris = table_barang.rowAtPoint(evt.getPoint());
        String nim = table_barang.getValueAt(baris, 1).toString();
        txt_idBarang.setText(nim);
        txt_idBarang.disable();
        if (table_barang.getValueAt(baris, 2)==null) {
            txt_namaBarang.setText("");
        } else {
            txt_namaBarang.setText(table_barang.getValueAt(baris, 2).toString());
        }
        if (table_barang.getValueAt(baris, 3)==null) {
            cb_pilihKategori.setSelectedItem(this);
        } else {
            cb_pilihKategori.setSelectedItem(table_barang.getValueAt(baris, 3).toString());
        }
        if (table_barang.getValueAt(baris, 4)==null) {
            txt_brand.setText("");
        } else {
            txt_brand.setText(table_barang.getValueAt(baris, 4).toString());
        }
        if (table_barang.getValueAt(baris, 5)==null) {
            txt_stock.setText("");
        } else {
            txt_stock.setText(table_barang.getValueAt(baris, 5).toString());
        }
        if (table_barang.getValueAt(baris, 6)==null) {
            txt_hargaBeli.setText("");
        } else {
            txt_hargaBeli.setText(table_barang.getValueAt(baris, 6).toString());
        }
        if (table_barang.getValueAt(baris, 7)== null) {
            txt_hargaJual.setText("");
        } else {
            txt_hargaJual.setText(table_barang.getValueAt(baris, 7).toString());
        }
        if (table_barang.getValueAt(baris, 8)== null) {
            cb_satuan.setSelectedItem("");
        } else {
            cb_satuan.setSelectedItem(table_barang.getValueAt(baris, 8).toString());
        }
    }//GEN-LAST:event_table_barangMouseClicked

