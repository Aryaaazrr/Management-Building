    private void autoIDBarang() {
        try {
            String sql = "SELECT * FROM barang ORDER BY id_barang DESC";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String idBarang = rs.getString("id_barang").substring(3);
                String id = "" + (Integer.parseInt(idBarang)+1);
                String angka = "";
                
                if (id.length()==1) {
                    angka = "0000";
                } else if (id.length()==2) {
                    angka = "000";
                } else if (id.length()==3){
                    angka = "00";
                } else if (id.length()==4) {
                    angka = "0";
                } else {
                    angka = "";
                }
                txt_idBarang.disable();
                txt_idBarang.setText("BRG" + angka + id);
            } else {
                txt_idBarang.setText("BRG00001");
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            System.out.println("autonumber");
        }
    }
