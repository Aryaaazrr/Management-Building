    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        try {
            String sql = "UPDATE pegawai SET id_pegawai = '"+cb_pilihIDPegawai.getSelectedItem()+"',"
                    + "nama_pegawai = '"+txt_namaPegawai.getText()+"',"
                    + "alamat = '"+txt_alamat.getText()+"', "
                    + "no_telp = '"+txt_noHP.getText()+"'"
                    + "WHERE user = '"+cb_pilihUser.getSelectedItem()+"'";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            this.setVisible(false);
            new pegawai().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            this.setVisible(false);
            new tambahDataPegawai().setVisible(true);
        }
        kosong();
    }//GEN-LAST:event_btn_tambahActionPerformed

